<h3>四則演算</h3>
<?php
    $data1 = 10;
    $data2 = 3;

    // 足し算
    print $data1 + $data2;
    print "<br />";

    // 引き算
    print $data1 - $data2;
    print "<br />";

    // 掛け算
    print $data1 * $data2;
    print "<br />";
    
    // 割り算
    print $data1 / $data2;
    print "<br />";

    // 余り
    print $data1 % $data2;
    print "<br />";
?>
